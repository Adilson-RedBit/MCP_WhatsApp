const DICTIONARIES: Record<Locale, Dictionary> = { "pt-BR": ptBR, es, en };

/**
 * Busca a tradução de `key` para `locale`. Fallback: pt-BR → a
 * própria chave (assim uma chave faltante aparece de forma óbvia
 * em dev, sem quebrar a UI).
 */
export function translate(locale: Locale, key: string): string {
  return DICTIONARIES[locale][key] ?? DICTIONARIES[DEFAULT_LOCALE][key] ?? key;
}
